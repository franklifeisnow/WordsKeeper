@Entity(generateConstructors = false)
public class Stu {
    @Id
    private long id;
    private String name;
    public Stu() {
    }
    public long getId() {
        return this.id;
    }
    public void setId(long id) {
        this.id = id;
    }
    public String getName() {
        return this.name;
    }
    public void setName(String name) {
        this.name = name;
    }
}
